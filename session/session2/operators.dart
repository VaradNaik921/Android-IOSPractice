void main() {
  var a = 15;
  var b = 10;
  //Operators:
  //1)Arithmetic Operator(+,-,*,/,%,~/):
  print(a+b);
  print(a-b);
  print(a*b);
  print(a/b);
  print(a%b);
  print(a~/b);
  //2)Relational Operator(>,<,>=,<=,==,!=,Test(is, is!)):
  print(a>b);
  print(a<b);
  print(a>=b);
  print(a<=b);
  print(a==b);
  print(a!=b);
  // print(a is! int);
  // print((a is int));
  //3)Increment/Decrement Operator:
  print(a++);
  print(a);
  print(a--);
  print(a);
  print(++a);
  print(a);
  print(--a);
  print(a);
  //4)Logical Operator(&&,||,!)
  print(a == 10 && b == 10);
  print(a == 10 || b == 10);
  print(!(a == 10) && b == 10);
}